_FIGURE_COUNTER = 0

def add_figure(doc, image_path, caption):
    global _FIGURE_COUNTER
    if not os.path.exists(image_path):
        print(f"ВНИМАНИЕ: Изображение {image_path} не найдено!")
        return
    _FIGURE_COUNTER += 1
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    run.add_picture(image_path, width=Cm(16))
    
    p_cap = doc.add_paragraph()
    p_cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run_cap = p_cap.add_run(f"Рисунок {_FIGURE_COUNTER} — {caption}")
    run_cap.italic = True
    run_cap.font.size = Pt(14)
    run_cap.font.name = "Times New Roman"
    
    doc.add_paragraph()

