        "Ахо А. В., Хопкрофт Д., Ульман Дж. Д. Структуры данных и алгоритмы – М.: Вильямс, 2003. - 384 с., ил.",
        "Кнут Д. Э. Искусство программирования, том 1. Основные алгоритмы, 3-е издание – М.: Вильямс, 2000. - 720 с., ил.",
        "MDN Web Docs: https://developer.mozilla.org/"
