    # 5. Сравнение
    add_heading(doc, "5. Сравнительный анализ алгоритмов", 1)
    add_heading(doc, "5.1. Теоретический анализ временной сложности", 2)
    add_paragraph(doc, "1. Алгоритм Дейкстры (с бинарной кучей): Алгоритм извлекает каждую вершину из очереди один раз O(V log V) и релаксирует каждое ребро один раз O(E log V). Итоговая временная сложность составляет O((V + E) log V).")
    add_paragraph(doc, "2. Алгоритм Беллмана-Форда: Выполняет релаксацию всех E рёбер V-1 раз. Временная сложность O(V * E).")
    add_paragraph(doc, "3. Алгоритм поиска в ширину (BFS): Работает только для невзвешенных графов. Временная сложность O(V + E).")
    add_paragraph(doc, "Вывод: Для графов с неотрицательными весами алгоритм Дейкстры является значительно более эффективным, чем Беллман-Форд, особенно на разреженных графах.")

    add_heading(doc, "5.2. Теоретический анализ пространственной сложности", 2)
    add_paragraph(doc, "1. Алгоритм Дейкстры: Требует памяти для хранения графа O(V + E), очереди с приоритетами O(V) и массивов расстояний/посещений O(V). Итоговая пространственная сложность: O(V + E).")
    add_paragraph(doc, "2. Алгоритм Флойда-Уоршелла: Требует матрицу смежности размером V x V. Пространственная сложность O(V^2).")
    add_paragraph(doc, "Вывод: Алгоритм Дейкстры экономно расходует память, масштабируясь линейно от размеров графа.")

