    add_numbered(doc, [
        "Титульный лист",
        "Введение",
        "Описание используемых методов и алгоритмов",
        "Особенности программной реализации алгоритмов",
        "Сравнительный анализ алгоритмов",
        "Листинги программ",
        "Распечатки содержимого входного и выходного файлов",
        "Список использованных источников",
        "Приложение №1"
    ])
    doc.add_page_break()

    # 2. Введение
    add_heading(doc, "2. Введение", 1)
    add_paragraph(doc, "В курсовой работе рассматривается задача поиска кратчайшего пути во взвешенном графе при неотрицательных весах рёбер. Данная задача является одной из классических и наиболее известных задач теории графов и комбинаторной оптимизации.")
    add_paragraph(doc, "Актуальность данной темы заключается в том, что алгоритмы поиска кратчайших путей находят широкое применение за пределами чистой математики: в маршрутизации сетевого трафика, логистике, навигационных системах (GPS), а также при проектировании топологий интегральных схем.")
    add_paragraph(doc, "В математической теории выделяют несколько алгоритмов решения данной задачи: алгоритм поиска в ширину (для невзвешенных графов), алгоритм Дейкстры, алгоритм Беллмана-Форда и алгоритм Флойда-Уоршелла.")
    add_paragraph(doc, "По заданию основным выбран алгоритм Дейкстры. Цель работы — самостоятельная реализация алгоритма и его сравнительный анализ с другими методами.")

    # 3. Описание используемых методов
    add_heading(doc, "3. Описание используемых методов и алгоритмов", 1)
    add_heading(doc, "3.1. Теоретические основы поиска кратчайших путей", 2)
    add_paragraph(doc, "Граф задаётся множеством вершин V и множеством рёбер E (допускаются как ориентированные, так и неориентированные графы). Каждому ребру приписан неотрицательный вес, определяющий \"стоимость\" прохождения.")
    add_paragraph(doc, "Важнейшим свойством кратчайших путей является принцип оптимальности: любой подпуть кратчайшего пути сам по себе является кратчайшим путем между своими конечными вершинами.")

    add_heading(doc, "3.2. Алгоритм Дейкстры", 2)
    add_paragraph(doc, "Алгоритм Дейкстры основывается на принципе «жадного» поиска и методе релаксации рёбер. Его суть заключается в последовательном нахождении кратчайших путей от стартовой вершины ко всем остальным.")
    add_paragraph(doc, "Пошаговое математическое описание алгоритма:")
    add_numbered(doc, [
        "Инициализация: Расстояние до стартовой вершины полагается равным 0, до всех остальных — бесконечности.",
        "Стартовая вершина помещается в очередь с приоритетом (где приоритет равен текущему известному расстоянию).",
        "Итерационный шаг: Извлекается вершина с минимальным расстоянием из очереди.",
        "Выполняется релаксация всех исходящих из неё рёбер к соседним вершинам: если текущее расстояние до соседа больше, чем расстояние до извлеченной вершины плюс вес ребра, то расстояние до соседа обновляется.",
        "При улучшении пути соседняя вершина добавляется в очередь (или обновляется её приоритет), а также обновляется предшественник для восстановления пути."
    ])
    add_paragraph(doc, "Алгоритм завершает работу, когда очередь с приоритетами становится пустой или когда извлечена целевая вершина.")

    # 4. Особенности
    add_heading(doc, "4. Особенности программной реализации алгоритмов", 1)
    add_heading(doc, "4.1. Выбор структур и типов данных", 2)
    add_paragraph(doc, "Для реализации алгоритма на языке JavaScript используются следующие структуры данных:")
    add_bullets(doc, [
        "Объекты (Objects) и Массивы (Arrays) для хранения списка смежности графа.",
        "Класс PriorityQueue — собственная реализация очереди с приоритетами на основе бинарной кучи (Min-Heap). Использование бинарной кучи позволяет извлекать минимальный элемент за логарифмическое время O(log V).",
        "Коллекции Map — для хранения текущих кратчайших расстояний (distances) и массива предшественников (predecessors).",
        "Коллекция Set — для хранения множества уже посещенных вершин (visited)."
    ])
    add_paragraph(doc, "В JavaScript числа представлены типом Number (стандарт IEEE 754), что полностью покрывает требования к хранению весов рёбер и расстояний, включая константу Infinity.")

    add_heading(doc, "4.2. Организация файлового ввода-вывода", 2)
    add_paragraph(doc, "Согласно требованиям, программа взаимодействует с пользователем через файлы:")
    add_bullets(doc, [
        "Входной файл (JSON-формат): Должен содержать массивы nodes (вершины) и edges (рёбра). Программа считывает файл через FileReader API и выполняет строгую валидацию структуры с помощью функции validateGraphData.",
        "Выходной файл: В него записывается результат работы в форматах JSON и TXT. Результат включает в себя путь path, общее расстояние distance, массивы distances и predecessors, а также метрики производительности. Также реализована генерация PDF-отчёта."
    ])

    add_heading(doc, "4.3. Архитектура и структура программы", 2)
    add_paragraph(doc, "Программа спроектирована по модульному принципу:")
    add_numbered(doc, [
        "graph.js — модуль структуры графа и валидации данных (GraphManager).",
        "dijkstra.js — вычислительный модуль, реализующий алгоритм Дейкстры (DijkstraAlgorithm) и бинарную кучу (PriorityQueue).",
        "visualizer.js — модуль пошаговой визуализации работы алгоритма на графе.",
        "ui.js — главный контроллер пользовательского интерфейса, связывающий логику и представление.",
        "storage.js — модуль для работы с LocalStorage, импортом, экспортом данных и формированием PDF-отчётов.",
        "tests/unit-tests.js — модуль автоматического тестирования ядра программы."
    ])

    add_heading(doc, "4.4. Разработка тестовых сценариев", 2)
    add_paragraph(doc, "Для верификации программы составлена таблица тестовых случаев, проверяемых автоматически:")
